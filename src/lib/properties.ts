export async function updateProperty(id: string, property: Partial<Property>): Promise<Property | null> {
    try {
        const { id: _, ...rest } = property;

        const dbProperty: Record<string, unknown> = {};
        if (rest.title !== undefined) dbProperty.title = rest.title;
        if (rest.type !== undefined) dbProperty.type = rest.type;
        if (rest.price !== undefined) dbProperty.price = rest.price;
        if (rest.image !== undefined) dbProperty.image = rest.image;
        if (rest.images !== undefined) dbProperty.images = rest.images;
        if (rest.location !== undefined) dbProperty.location = rest.location;
        if (rest.neighborhood !== undefined) dbProperty.neighborhood = rest.neighborhood;
        if (rest.code !== undefined) dbProperty.code = rest.code;
        if (rest.bedrooms !== undefined) dbProperty.bedrooms = rest.bedrooms;
        if (rest.bathrooms !== undefined) dbProperty.bathrooms = rest.bathrooms || 0;
        if (rest.suites !== undefined) dbProperty.suites = rest.suites;
        if (rest.garage !== undefined) dbProperty.garage = rest.garage || 0;
        if (rest.area !== undefined) dbProperty.area = rest.area;
        if (rest.landArea !== undefined) dbProperty.land_area = rest.landArea;
        if (rest.builtArea !== undefined) dbProperty.built_area = rest.builtArea;
        if (rest.description !== undefined) dbProperty.description = rest.description;
        if (rest.features !== undefined) dbProperty.features = rest.features || [];

        const { data, error } = await supabase
            .from('properties')
            .update(dbProperty)
            .eq('id', id)
            .select()
            .single();

        if (error) {
            console.error('Error updating property in Supabase:', error);
            throw error;
        }

        if (data) {
            return {
                ...data,
                landArea: data.land_area,
                builtArea: data.built_area,
                id: String(data.id)
            };
        }
        return null;
    } catch (error) {
        console.error('Unexpected error updating property:', error);
        throw error;
    }
}

export async function deleteProperty(id: string): Promise<boolean> {
    try {
        const { error } = await supabase
            .from('properties')
            .delete()
            .eq('id', id);

        if (error) {
            console.error('Error deleting property from Supabase:', error);
            return false;
        }
        return true;
    } catch (error) {
        console.error('Unexpected error deleting property:', error);
        return false;
    }
}

