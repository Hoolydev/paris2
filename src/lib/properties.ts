export async function getSimilarProperties(type: string, location: string, currentId: string): Promise<Property[]> {
    try {
        const { data, error } = await supabase
            .from('properties')
            .select('*')
            .neq('id', currentId)
            .or(`type.eq.${type},location.eq.${location}`)
            .limit(4);

        if (error) {
            console.error('Error fetching similar properties:', error);
            return [];
        }

        return (data || []).map((p: any) => ({
            ...p,
            landArea: p.land_area,
            builtArea: p.built_area,
            id: String(p.id)
        }));
    } catch (error) {
        console.error('Unexpected error fetching similar properties:', error);
        return [];
    }
}

export interface SearchFilters {
    type?: string;
    neighborhood?: string;
    bedrooms?: number;
    minPrice?: number;
    maxPrice?: number;
    query?: string;
}

export async function searchProperties(filters: SearchFilters): Promise<Partial<Property>[]> {
    try {
        let query = supabase
            .from('properties')
            .select('id, title, type, neighborhood, price, bedrooms, bathrooms, area, land_area, built_area, garage, images, location, description')
            .order('created_at', { ascending: false });

        if (filters.type) {
            query = query.ilike('type', `%${filters.type}%`);
        }

        if (filters.neighborhood) {
            query = query.ilike('neighborhood', `%${filters.neighborhood}%`);
        }

        if (filters.bedrooms) {
            query = query.gte('bedrooms', filters.bedrooms);
        }

        if (filters.minPrice) {
            query = query.gte('price', filters.minPrice);
        }

        if (filters.maxPrice) {
            query = query.lte('price', filters.maxPrice);
        }

        const { data, error } = await query;

        if (error) {
            console.error('Error searching properties:', error);
            return [];
        }

        let results = (data || []).map((p: any) => ({
            id: String(p.id),
            title: p.title,
            type: p.type,
            neighborhood: p.neighborhood,
            price: p.price,
            bedrooms: p.bedrooms,
            bathrooms: p.bathrooms,
            area: p.area,
            landArea: p.land_area,
            builtArea: p.built_area,
            garage: p.garage,
            images: p.images || [],
            location: p.location,
            description: p.description,
        }));

        // Client-side free-text filter on title, description, location
        if (filters.query) {
            const q = filters.query.toLowerCase();
            results = results.filter(p =>
                (p.title && p.title.toLowerCase().includes(q)) ||
                (p.description && p.description.toLowerCase().includes(q)) ||
                (p.location && p.location.toLowerCase().includes(q)) ||
                (p.neighborhood && p.neighborhood.toLowerCase().includes(q))
            );
        }

        return results;
    } catch (error) {
        console.error('Unexpected error searching properties:', error);
        return [];
    }
}
